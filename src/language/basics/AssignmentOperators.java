package language.basics;

public class AssignmentOperators {

    public static void main(String[] args) {
        // Operators can be used to assign values to variable

        int x = 10;
        System.out.println(x);

        x += 10;
        System.out.println(x);

        x -= 10;
        System.out.println(x);

        x *= 10;
        System.out.println(x);

        x /= 10;
        System.out.println(x);

    }

}